/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = function(fn, args, t) {
    /*
    We call the function once to start the timer for fn.
    Then we replace it with the cancel function, so calling it stops the timer anytime.

    // Start timer to run fn after t milliseconds
    const cancel = cancellable(fn, args, t);

    // Later, call cancel to stop fn from running if timer not done
    cancel();
    */
    const timer = setTimeout(() => fn(...args), t);
    return () => clearTimeout(timer);
};

